tmp = img.crop((128,46,144,62))
target.paste(tmp,(0,48),tmp)
tmp = img.crop((144,46,160,62))
target.paste(tmp,(0,64),tmp)
tmp = img.crop((160,46,176,62))
target.paste(tmp,(0,80),tmp)
target.save('result/dragon.png')
